     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Questionare $questionare)
    {
        $validate = request()->validate([
           'question.question' => 'required|min:5',
            'answers.*.answer' => 'required'
        ]);

        $question = $questionare->questions()->create($validate['question']);
        $question->answers()->createMany($validate['answers']);
        return  redirect('/questionare/'.$questionare->id);
    }

